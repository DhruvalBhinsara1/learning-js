//Shift Operator

//Two variations of shift are possible ïƒ  left and right shift

//1.left shift: shift bits in left direction for specified value
//2.right shift: shift bits in right direction for specified value

//The left shift operator (<<) multiplies a number by 2 raised to the power of the number of places specified by the right operand.

//Example:
var a = 7, b = 5;
console.log("a<<b : " + (a << b));  // multiply by 2, left shift

//Output
//a << b = 32


//The right shift operator (>>) divides a number by 2 raised to the power of the number of places specified by the right operand.

//Example 
console.log("a>>b : " + (a >> b)); // divide by 2, right shift

//Output
//a >> b = 2 
