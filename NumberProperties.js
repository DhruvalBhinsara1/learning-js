//Number Properties

//there are 4 types of number properties in JavaScript

//1.Max_Value
//2.Min_Value
//3.Positive_Infinity
//4.Negative_Infinity

//exmaple
console.log(x = 1 / 0); //output: Infinity

console.log(x = 1 / 0.1); //output: 10

console.log(x = Number.POSITIVE_INFINITY); //output: Infinity

console.log(x = Number.NEGATIVE_INFINITY); //output: -Infinity


//Min and Max Safe Integers 
//We use BigInt when this numbers end.

console.log(Number.MIN_SAFE_INTEGER) //output: -9007199254740991

console.log(Number.MAX_SAFE_INTEGER) //output: 9007199254740991

