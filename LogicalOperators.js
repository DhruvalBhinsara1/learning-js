//Logical Operators

// Logical AND (&&) operator returns true if both operands are true.

//example:

let a = true;
let b = false;

console.log(a && b); // false

// Logical OR (||) operator returns true if either of the operands is true.

//example:

let c = true;
let d = false;

console.log(c || d); // true


// Logical NOT (!) operator returns true if operand is false.

//example:

let e = true;

console.log(!e); // false

let f = false;

console.log(!f); // true

