//Relational Operators
//Following are relational operators:
//== (equal value)
//===(equal value and type check)
//!= (not equal value)
// <, >, <=, >= ( (greater or equal value)


//differences between == and ===:


let a = 10;
let b = 20;

console.log(a > b); // true

console.log(a < b); // false

console.log(a >= b); // false

console.log(a <= b); // true

console.log(a == b); // false (value comparison)

console.log(a === b); // false (type and value comparison)

console.log(+0 == -0); // true (value comparison)

console.log(+0 === -0); // false (type comparison)

