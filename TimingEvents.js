//Timing Events

//JavaScript provides two types of timing events:

//The setTimeout() method calls a function or evaluates an expression after a specified number of milliseconds.

//example:

window.setTimeout(function () {
    alert("Legendary!!!");
}, 200);


//The setInterval() method calls a function or evaluates an expression repeatedly, with a fixed time delay between each call.

//example:

