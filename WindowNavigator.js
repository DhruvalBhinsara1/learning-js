//Window Navigator

//Window Navigator provides information about the browser and its environment.

//You can use window.navigator to access the navigator object.

//Properties of window.navigator:

//navigator.appName: The name of the application

console.log(navigator.appName); // Output: Netscape

//navigator.appVersion: The version of the application

console.log(navigator.appVersion); // Output: 5.0 (Windows NT 10.0; Win64; x64)

//navigator.userAgent: A string containing information about the browser and its version

console.log(navigator.userAgent); // Output: Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36

//navigator.platform: The platform on which the browser is running

console.log(navigator.platform); // Output: Win64

//navigator.online :function that returns true if the browser is online, otherwise false

console.log("the person is", navigator.online ? "online" : "offline"); // Output: true or false

