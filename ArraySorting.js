//Array Sorting

//sort method sorts the array in alphabetical order

var fruits = [2, 51, 52, 62, 765];
fruits.sort();        // Sorts the elements of fruits
console.log(fruits); //Output: [ 'Apple', 'Banana', 'Mango', 'Orange' ]


//reverse method reverses the order of the elements in the array
//reverse is not alphabetical order

console.log(fruits.reverse());
//Output: [ 'Orange', 'Mango', 'Banana', 'Apple' ]

