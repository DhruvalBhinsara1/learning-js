//unary operators
//Unary operators are used to perform operation on a single operand.
//It needs 1 operand
//There are 2 major operators:
//1. increment (++)
//2. decrement (--)
//both have 2 types: pre-increment, post-increment, pre-decrement, post-decrement

//pre-increment
let x = 5;
let y = ++x;
console.log("pre-increment");
console.log(y); //6
console.log(x); //6

//post-increment
let a = 5;
let b = a++;
console.log("post-increment");
console.log(b); //5
console.log(a); //6

//pre-decrement
let c = 5;
let d = --c;
console.log("pre-decrement");
console.log(d); //4
console.log(c); //4

//post-decrement
let e = 5;
let f = e--;
console.log("post-decrement");
console.log(f); //5
console.log(e); //4


